#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	nb;
	char	*dest;
	int		a;

	a = 0;
	nb = ft_strlen(src);
	if (!(dest = (char*)malloc(sizeof(char) * (nb + 1))))
		return (NULL);
	while (src[a] != '\0')
	{
		dest[a] = src[a];
		a++;
	}
	dest[a] = '\0';
	return (dest);
}
